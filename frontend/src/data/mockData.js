// Utility function to get poster URL
export const getPosterUrl = (posterPath) => {
  return posterPath ? `https://image.tmdb.org/t/p/w500${posterPath}` : 'https://image.tmdb.org/t/p/w500/default.jpg';
};

// Utility function to get backdrop URL
export const getBackdropUrl = (backdropPath) => {
  return backdropPath ? `https://image.tmdb.org/t/p/original${backdropPath}` : 'https://image.tmdb.org/t/p/original/default.jpg';
};

